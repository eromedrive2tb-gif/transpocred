<script>
    document.addEventListener('DOMContentLoaded', function () {
        function initSlider() {
            if (typeof Swiper !== 'undefined') {
                // Destruir instância anterior se existir para garantir nova init
                if (window.bannerSliderPrincipal && typeof window.bannerSliderPrincipal.destroy === 'function') {
                    window.bannerSliderPrincipal.destroy(true, true);
                }

                window.bannerSliderPrincipal = new Swiper('.banner-principal', {
                    loop: true,
                    autoplay: {
                        delay: 5000,
                        disableOnInteraction: false,
                    },
                    pagination: {
                        el: '.banner-pagination',
                        clickable: true,
                    },
                    effect: 'fade',
                    fadeEffect: {
                        crossFade: true
                    },
                    speed: 1000,
                    observer: true,
                    observeParents: true
                });
            } else {
                setTimeout(initSlider, 500);
            }
        }
        initSlider();
    });
</script>